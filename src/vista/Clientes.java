        controllerClientes.setNomb(this.Nombres.getText());
        controllerClientes.setDni(this.Dni.getText());
        controllerClientes.setDir(this.Direccion.getText());
        controllerClientes.setRuc(this.Ruc.getText());
        controllerClientes.setFono(this.Telefono.getText());
