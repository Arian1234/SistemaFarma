    public void insertProveedores() {
        if (validator.novacios("1-11", 2, this.Proveedor,this.Ruc) == false) {

            controllerProveedores.setOp("I");
            controllerProveedores.setCod(0);
            controllerProveedores.setNomb(this.Proveedor.getText());
            controllerProveedores.setCel(this.Celular.getText());
            controllerProveedores.setDir(this.Direccion.getText());
            controllerProveedores.setCorreo(this.Correo.getText());
            controllerProveedores.setFono(this.Telefono.getText());
            controllerProveedores.setRuc(this.Ruc.getText());
            
            this.txtmsg.setText(controllerProveedores.mntoProveedor());

            controllerProveedores.setOp("D");
            controllerProveedores.setBusc(this.Proveedor.getText());
            controllerProveedores.consultarProveedor("PROVEE",dtm);
            clear();
        }
    }

    public void updateProveedores() {
        if (validator.novacios("1-11", 2, this.Proveedor,this.Ruc) == false) {

            controllerProveedores.setOp("U");
            controllerProveedores.setCod(Integer.parseInt(this.txtcodprovee.getText()));
            controllerProveedores.setRuc(this.Ruc.getText());
            controllerProveedores.setNomb(this.Proveedor.getText());
            controllerProveedores.setCel(this.Celular.getText());
            controllerProveedores.setDir(this.Direccion.getText());
            controllerProveedores.setCorreo(this.Correo.getText());
            controllerProveedores.setFono(this.Telefono.getText());

            this.txtmsg.setText(controllerProveedores.mntoProveedor());

            controllerProveedores.setOp("D");
            controllerProveedores.setBusc(this.Proveedor.getText());
            controllerProveedores.consultarProveedor("PROVEE",dtm);
            clear();
        }
    }

