    private void ProveedorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ProveedorKeyPressed
        if (evt.getExtendedKeyCode() == KeyEvent.VK_F4) {
            clear();
        } else {
            if (evt.getExtendedKeyCode() == KeyEvent.VK_F5) {
                insertProveedores();
            } else {
                if (evt.getExtendedKeyCode() == KeyEvent.VK_F6) {
                    updateProveedores();
                }
            }

        }

    }//GEN-LAST:event_ProveedorKeyPressed

