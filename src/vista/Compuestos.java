    public void clear() {
        this.txtcompu.setText(null);
        this.Nombre.setText(null);
        this.Descripcion.setText(null);
        this.Nombre.requestFocus();
    }

    public void insertCompuestos() {
        if (validator.novacios("1", 1, this.Nombre) == false) {
            controllerCompuestos.setOp("I");
        }
        controllerCompuestos.setCod(0);
        controllerCompuestos.setNomb(this.Nombre.getText());
        controllerCompuestos.setDescrip(this.Descripcion.getText());
        this.txtmsg.setText(controllerCompuestos.mntoCompuestos());

        controllerCompuestos.setOp("D");
        controllerCompuestos.setBusc(this.Nombre.getText());
        controllerCompuestos.consultarCompuestos(dtm);
        clear();
    }

    public void actualizarCompuestos() {
        controllerCompuestos.setOp("U");
        controllerCompuestos.setCod(Integer.parseInt(this.txtcompu.getText()));
        controllerCompuestos.setNomb(this.Nombre.getText());
        controllerCompuestos.setDescrip(this.Descripcion.getText());
        this.txtmsg.setText(controllerCompuestos.mntoCompuestos());

        controllerCompuestos.setOp("D");
        controllerCompuestos.setBusc(this.Nombre.getText());
        controllerCompuestos.consultarCompuestos(dtm);
        clear();
    }

